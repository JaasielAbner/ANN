import math
import numpy as np

def ajuste_exponencial(x, y):
    n = len(x)

    somaX  = sum(x)
    somaX2 = sum(xi ** 2 for xi in x)
    logY   = [math.log(yi) for yi in y]
    somaY  = sum(logY)
    somaXY = sum(xi * yi for xi, yi in zip(x, logY))

    A = [[n, somaX], [somaX, somaX2]]
    B = [somaY, somaXY]
    
    a0, a1 = np.linalg.solve(A, B)
    a, b = math.exp(a0), a1

    return a,b
