import numpy as np

def regressao(x, y):

    n = len(x)
    somaX  = sum(x)
    somaX2 = sum(xi ** 2 for xi in x)
    somaY  = sum(y)
    somaXY = sum(yi * xi for xi, yi in zip(x,y))
    A = [[n, somaX],[somaX, somaX2]]
    B = [somaY, somaXY]
    a0, a1 = np.linalg.solve(A,B)

    return a0, a1 # a0 é coef linear
                  # a1 é coef angular

